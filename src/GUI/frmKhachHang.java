    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        ClearDataTable();
        List<KhachHang> LKH = ctl.TimKiemKhachHang(txtTimKiem.getText());
        if(LKH != null){
            for (int i = 0; i < LKH.size(); i++) {
                Object[] rows = new Object[7];
                rows[0] = LKH.get(i).getMaKH();
                rows[1] = LKH.get(i).getTenKH();
                rows[2] = LKH.get(i).getSDT();
                rows[3] = LKH.get(i).getEmail();
                rows[4] = LKH.get(i).getGioiTinh();
                rows[5] = LKH.get(i).getDiaChi();
                rows[6] = LKH.get(i).getNgaySinh();
                dt.addRow(rows);
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

