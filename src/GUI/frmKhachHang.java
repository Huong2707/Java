    private void showDataTable() {
            List<KhachHang> listKH = ctl.getKhachHang();
            for(int i = 0; i < listKH.size(); i++) {
                Object[] rows = new Object[7];
                rows[0] = listKH.get(i).getMaKH();
                rows[1] = listKH.get(i).getTenKH();
                rows[2] = listKH.get(i).getSDT();
                rows[3] = listKH.get(i).getEmail();
                rows[4] = listKH.get(i).getGioiTinh();
                rows[5] = listKH.get(i).getDiaChi();
                rows[6] = listKH.get(i).getNgaySinh();
                dt.addRow(rows);
            }

   
    }
    
    

    

