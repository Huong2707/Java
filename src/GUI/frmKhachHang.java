    private void jtbQLKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbQLKHMouseClicked
        // TODO add your handling code here:
        int r = jtbQLKH.getSelectedRow();
        if(r>= 0){
            txtMaKH.setText(jtbQLKH.getValueAt(r, 0).toString());
            txtTenKH.setText(jtbQLKH.getValueAt(r, 1).toString());
            txtSoDT.setText(jtbQLKH.getValueAt(r, 2).toString());
            txtEmail.setText(jtbQLKH.getValueAt(r, 3).toString());
            txtGioiTinh.setText(jtbQLKH.getValueAt(r, 4).toString());
            txtDiaChi.setText(jtbQLKH.getValueAt(r, 5).toString());
            txtNgaySinh.setText(jtbQLKH.getValueAt(r, 0).toString());
            
            txtMaKH.setEnabled(false);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_jtbQLKHMouseClicked

